dust = 45

if dust > 150 : 
    print("매우 나쁨")
elif dust > 80 : 
    print("나쁨")
elif dust > 30 :
    print("보통")
else :
    print("좋음")


# dust 변수에 들어있는 값을 기준으로 미세먼지 정보출력
# dust 가 150보다 크다 : 매우나쁨
# dust 가 80보다 크고 150이하이다 : 나쁨
# dust 가 30보다 크고 80이하이다 : 보통
# dust 가 30이하이다 : 좋음
# 이렇게 출력하는 조건문을 작성해봅시다. 